package projetonn_cet.util;

/**
 *
 * @author mpcsj
 */
public class Agregado {

    double Al2O3, CaO, Fe2O3, K2O, MgO, P2O5, SiO2, TiO2;

    public Agregado(double a, double b, double c, double d, double e, double f, double g, double h) {
        Al2O3 = a;
        CaO = b;
        Fe2O3 = c;
        K2O = d;
        MgO = e;
        P2O5 = f;
        SiO2 = g;
        TiO2 = h;
    }
}
